package String.String_Method;

public class toLowerCaseMethod1 {
    public static void main(String[] args) {
        String str = "HELLO WORLD 123";
        String lowerCaseStr = str.toLowerCase();
        System.out.println(lowerCaseStr); // Output: hello world 123
        System.out.println("Original String: " + str); // Output: Original String: HELLO WORLD 123
    }
}

package String.String_Method;

public class toUpperCaseMethod1 {
    public static void main(String[] args) {
        String text = "java is fun";
        String upperCaseStr = text.toUpperCase();
        System.out.println(upperCaseStr); // Output: JAVA IS FUN
        System.out.println("Original String: " + text); // Output: Original String: java is fun
    }
}

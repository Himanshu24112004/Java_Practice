package String.String_Method;

public class isEmptyMethod1 {
    public static void main(String[] args) {
        String str1 = "";
        string str2 = " ";
        System.out.println("Is str1 empty? " + str1.isEmpty()); // true
        System.out.println("Is str2 empty? " + str2.isEmpty()); // false
    }
}

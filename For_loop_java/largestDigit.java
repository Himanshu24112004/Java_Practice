import java.util.Scanner;

public class largestDigit {
    public static void main(String[] args){
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
         int max = 0;
         for(;n>0;n/=10){
            int d= n%10;
            if(d>max){
                max = d;
            }
         }
         System.out.println(max);
         sc.close();
    }
}



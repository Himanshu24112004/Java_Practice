import java.util.Scanner;

public class productOfDigits {
    public static void main(String[] args){
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        int prod = 1;
        for(;n!=0; n/=10){
            prod *= n%10;
        }
        System.out.println("Product:"+ prod);
        sc.close();
    }
}



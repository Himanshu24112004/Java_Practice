import java.util.Scanner;

public class approximateE {
    public static void main(String[] args){
        Scanner sc =new Scanner(System.in);
        int n= sc.nextInt();
        double e= 1.0;
        double fact = 1.0;
        for(int i=1; i<=n; i++){
            fact *= i;
            e += 1.0/fact;
        }
        System.out.println(e);
        sc.close();
    } 
}



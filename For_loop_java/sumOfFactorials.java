import java.util.Scanner;

public class sumOfFactorials {
    public static void main(String[] args){
        Scanner sc =   new Scanner(System.in);
        int n=sc.nextInt();
        int fact = 1;
        int sum = 0;
        for(int i=1; i<=n; i++){
            fact *= i;
            sum += fact;
        }
        System.out.println(sum);
        sc.close();
    }
}



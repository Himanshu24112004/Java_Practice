import java.util.Scanner;

public class geneticRoot {
    public static void main(String[] args){
        Scanner sc = new Scanner(System.in);
        System.out.print("Enter  the number(in 99..... form): ");
        int n= sc.nextInt();
        for(;n>9;){
            int sum=0;
            int temp = n;
            for(;temp>0;temp/=10){
                sum = temp%10;
            }
            n = sum;
        }
        System.out.println("Genetic Root = "+n);
        sc.close();
    }
}



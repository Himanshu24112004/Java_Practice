import java.util.Scanner;

public class fabonacciSeries {
    public static void main(String[] args){
        Scanner sc =new Scanner(System.in);
        int n= sc.nextInt();
        int t1 = 0,  t2=1;

        System.out.println(t1+" "+t2+"");
        for(int i=3; i<=n; i++){
            int next = t1 + t2;
            System.out.println(next+" ");
            t1 = t2 ;
            t2 = next;
        }
        sc.close();
    }
}



import java.util.Scanner;

public class DecimalToBinary {
    public static void main(String[] args){
        Scanner sc= new Scanner(System.in);
        int n = sc.nextInt();

        for(int i=31; i>=0; i--){
            int k = n>>i;
            if((k & 1)== 1){
                System.out.print("1");
            }
            else{
                System.out.print("0");
            }
        }
        sc.close();
    }
}



import java.util.Scanner;

public class integerSquareRoot {
    public static void main(String[] args){
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        int root = 1;

        for(int i=1; i*i <= n; i++){
            root = i;
        }
        System.out.println("Square root (floor) = " + root);
        sc.close();
    }
}



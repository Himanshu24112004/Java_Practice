import java.util.Scanner;

public class HarshadNumber {
    public static void main(String[] args){
      Scanner sc = new Scanner(System.in);
        int n= sc.nextInt();
        int temp = n;
         int sum = 0;
         for(;temp!= 0; temp/=10){
            sum += temp%10;
         }
         if(n%sum==0){
            System.out.println("Harshad number");
         }
         else{
            System.out.println("Not Harshad number");
         }
         sc.close();
    }
}



import java.util.Scanner;

public class Scholarship {
    public static void main(String[] args){
        Scanner sc= new Scanner(System.in);
        System.out.print("Enter your Percentages :");
        double per = sc.nextDouble();
        System.out.print("Enter your Family Income :");
        double familyIncome = sc.nextDouble();

        if(per >= 85 && familyIncome<300000){
            System.out.println("Full Scholarship");
        }
        else if(per >= 70 && familyIncome <500000){
            System.out.println("Partial Scholarship");
        }
        else{
            System.out.println("Not Eligible");
        }
        sc.close();
    }
}


